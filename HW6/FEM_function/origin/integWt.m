function w = integWt(ndime,nelnd,M)
    w = zeros(M,1);
    if (ndime == 1)
        w(1,1) = 1;
        w(2,1) = 1;
    elseif (ndime == 2)
        if (nelnd == 3)
            w(1,1) = 1/2.;
        end
        if (nelnd == 6)
            w(1,1) = -27/96;
            w(2,1) = 25/96;
            w(3,1) = 25/96;
            w(4,1) = 25/96;
        end
        if (nelnd == 4)
            w(1,1) = 1;
            w(2,1) = 1;
            w(3,1) = 1;
            w(4,1) = 1;
        end
        if (nelnd == 8)
            w(1,1) = 0.308641974;
            w(2,1) = 0.493827159;
            w(3,1) = 0.308641974;
            w(4,1) = 0.493827159;
            w(5,1) = 0.790123455;
            w(6,1) = 0.493827159;
            w(7,1) = 0.308641974;
            w(8,1) = 0.493827159;
            w(9,1) = 0.308641974;       
        end

    elseif (ndime == 3)
        if (nelnd == 4) 
            w(1,1) = 1/4.; 
            w(2,1) = 1/4.;
            w(3,1) = 1/4.;
        end
        if (nelnd == 10) 
            w(:,1) = 1/24; 
        end
        if (nelnd == 8) 
            w(:,1) = 1; 
        end
        if (nelnd == 20) 
            w([1,3,7,9,19,21,25,27],1) = 0.171467763;
            w([2,4,6,8,10,12,16,18,20,22,24,26],1) = 0.274348421;
            w([5,11,13,15,17,23],1) = 0.438957474;
            w(14, 1) = 0.702331959;
        end
    end
end